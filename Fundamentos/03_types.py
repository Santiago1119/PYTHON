#strings (cadenas de caracteres)
my_name = "Santiago"

#int (numeros enteros)
my_age = 19
print('mi edad es', my_age)
print(type(my_age))

#boolean (verdadero o falso)
is_single = False
print('es soltero?', is_single)
print(type(is_single))

#una nueva función de python es "type" la cual nos permite saber que tipo de dato contiene la variable que se le para como parámetro
print('mi nombre es', my_name)
print(type(my_name))

#inputs (solicitar información) siempre cuando se ingresa un valor por consola el valor ingresa como string, así se ingrese un número

your_age = input('¿Cual es tu edad?')
print('tu edad es:', your_age)
print(type(your_age))